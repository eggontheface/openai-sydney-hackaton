import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath, pathToFileURL } from "node:url";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const defaultRootDir = path.resolve(scriptDir, "..");
const defaultOutputFile = path.join(
  defaultRootDir,
  "src/config/localOpenAiApiKey.generated.ts",
);

function stripOptionalQuotes(value) {
  if (
    (value.startsWith('"') && value.endsWith('"')) ||
    (value.startsWith("'") && value.endsWith("'"))
  ) {
    return value.slice(1, -1);
  }

  return value;
}

export function parseDotenv(contents) {
  const values = {};

  for (const line of contents.split(/\r?\n/)) {
    const trimmed = line.trim();
    if (!trimmed || trimmed.startsWith("#")) {
      continue;
    }

    const normalized = trimmed.startsWith("export ")
      ? trimmed.slice(7).trim()
      : trimmed;
    const separatorIndex = normalized.indexOf("=");
    if (separatorIndex === -1) {
      continue;
    }

    const key = normalized.slice(0, separatorIndex).trim();
    const value = normalized.slice(separatorIndex + 1).trim();
    if (key) {
      values[key] = stripOptionalQuotes(value);
    }
  }

  return values;
}

function localEnvValues(env) {
  const values = {};

  for (const key of ["LOCAL_EMBED_OPENAI_KEY", "OPENAI_API_KEY"]) {
    if (env[key] !== undefined) {
      values[key] = env[key];
    }
  }

  return values;
}

async function readDotenvLocal(rootDir) {
  try {
    const contents = await readFile(path.join(rootDir, ".env.local"), "utf8");
    return parseDotenv(contents);
  } catch (error) {
    if (error && typeof error === "object" && error.code === "ENOENT") {
      return {};
    }

    throw error;
  }
}

export function resolveLocalOpenAiApiKey({
  dotenv = {},
  env = process.env,
} = {}) {
  const values = {
    ...dotenv,
    ...localEnvValues(env),
  };

  if (values.LOCAL_EMBED_OPENAI_KEY !== "1") {
    return null;
  }

  const apiKey = values.OPENAI_API_KEY?.trim();
  if (!apiKey) {
    throw new Error(
      "LOCAL_EMBED_OPENAI_KEY=1 requires OPENAI_API_KEY to be set.",
    );
  }

  return apiKey;
}

export function renderLocalOpenAiApiKeyModule(apiKey) {
  return [
    "// Generated by scripts/generate-local-openai-key.mjs.",
    "// Do not commit this file with a real API key.",
    `export const localOpenAiApiKey = ${apiKey ? JSON.stringify(apiKey) : "null"} as string | null;`,
    "",
  ].join("\n");
}

export async function generateLocalOpenAiApiKeyFile({
  dotenv,
  env = process.env,
  outputFile = defaultOutputFile,
  rootDir = defaultRootDir,
} = {}) {
  const dotenvValues = dotenv ?? (await readDotenvLocal(rootDir));
  const apiKey = resolveLocalOpenAiApiKey({ dotenv: dotenvValues, env });
  const moduleSource = renderLocalOpenAiApiKeyModule(apiKey);

  await mkdir(path.dirname(outputFile), { recursive: true });
  await writeFile(outputFile, moduleSource);

  return {
    embedded: Boolean(apiKey),
    outputFile,
  };
}

const isDirectRun =
  typeof process.argv[1] === "string" &&
  import.meta.url === pathToFileURL(process.argv[1]).href;

if (isDirectRun) {
  const result = await generateLocalOpenAiApiKeyFile();
  const mode = result.embedded ? "demo key seed" : "empty key seed";
  console.log(
    `Generated ${mode}: ${path.relative(defaultRootDir, result.outputFile)}`,
  );
}
